How to create your gun model:
You need a part called 'BodyAttach' in your GunModel. You also need to rig the gun up properly
for animations. Oh forgot, you also need another part called 'FirePoint' inside the GunModel.

If you don't have these important parts in your gun, it may throw some warnings at you.
You can look at the documentation on how to fix these and what they are.





